#Definiendo una variable con camelCase
nombreCompleto = "Lucio"

#Definiendo una variable con snake_case (mas usada)
nombre_completo = "Lucio"

#Concatenar con f-strings
bienvenido = f"Hola {nombre_completo} ¿Como estas?" 

#Concatenar con +
bienvenido = "Hola " + " ¿Como estas?"

#Operadores de pertenencia (in / not in)
